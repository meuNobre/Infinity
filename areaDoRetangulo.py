print(f"Bem-vindo(a) à calculadora de área de retângulos. Digite a seguir as informações solicitadas. Siga como base a fórmula:\nÁrea = Base x Altura")

base = float(input("Digite a base do seu retângulo em cm (centímetros): "))
altura = float(input("Digite a altura do seu retângulo em cm (centímetros): "))
    
area = base * altura

print(f"A área do retângulo é {area}cm")
